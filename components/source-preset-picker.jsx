export function SourcePresetPicker({ value, onChange, mode = "multi" }) {
  const selectedSources = parseCommaSeparatedList(value);

  const toggleSource = (source) => {
    if (mode === "single") {
      onChange(source);
      return;
    }

    const isSelected = selectedSources.includes(source);
    const nextSources = isSelected
      ? selectedSources.filter((item) => item !== source)
      : [...selectedSources, source];

    onChange(nextSources.join(", "));
  };

  return (
    <div className="source-preset-grid" aria-label="Visibility source presets">
      {DEFAULT_VISIBILITY_SOURCES.map((source) => {
        const meta = sourceMeta[source] || { icon: null, tone: "neutral" };
        const Icon = meta.icon;
        const isSelected = selectedSources.includes(source);

        return (
          <button
            key={source}
            type="button"
            className={`source-preset source-preset-${meta.tone}${isSelected ? " source-preset-selected" : ""}`}
            onClick={() => toggleSource(source)}
            aria-pressed={isSelected}
          >
            <span className="source-preset-mark">
              {Icon ? <Icon /> : source.slice(0, 2).toUpperCase()}
            </span>
            <span className="source-preset-info">
              <span className="source-preset-name">{source}</span>
              <span className="source-preset-label">{meta.label}</span>
            </span>
            {isSelected && (
              <span className="source-preset-check" aria-hidden="true">
                <svg viewBox="0 0 12 12" fill="none">
                  <path d="M2 6l3 3 5-5" stroke="currentColor" strokeWidth="1.75" strokeLinecap="round" strokeLinejoin="round" />
                </svg>
              </span>
            )}
          </button>
        );
      })}
    </div>
  );
}
